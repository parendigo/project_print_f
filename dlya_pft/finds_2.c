#include "printf.h"

int		find_width(char *str)
{
	int		i;
	int		n;

	i = 1;
	n = 0;
	while (str[i])
	{
		if (ze_ni(str[i]) && str[i - 1] != '.' && ne_ze_ni(str[i - 1]))
			n = i;
		i++;
	}
	return (n == 0 ? 0 : n);
}
