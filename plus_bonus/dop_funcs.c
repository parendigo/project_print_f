int		lf_diouxx(char a)
{
	if (a == 'd' || a == 'i' || a == 'o' || a == 'u' || a == 'x' || a == 'X')
		return (1);
	return (0);
}

int		ze_ni(char a)
{
	if (a == '0' || a == '1' || a == '2' || a == '3' || a == '4' || a == '5')
		return (1);
	if (a == '6' || a == '7' || a == '8' || a == '9')
		return (1);
	return (0);
}

int		ne_ze_ni(char a)
{
	if (a != '0' && a != '1' && a != '2' && a != '3' && a != '4' && a != '5')
		if (a != '6' && a != '7' && a != '8' && a != '9')
			return (1);
	return (0);
}

